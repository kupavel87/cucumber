class UserForm(FlaskForm):
    id = StringField('list_id', render_kw={"class": "form-control", "hidden": ""})
    username = StringField('Имя', render_kw={"class": "form-control-plaintext", "readonly": ""})
    email = StringField('Email', render_kw={"class": "form-control", "readonly": ""})
    role = StringField('Роль', render_kw={"class": "form-control-plaintext", "readonly": ""})
    edit_user = SubmitField(edit_icon, render_kw={"class": "btn btn-success"})
    del_user = SubmitField(del_icon, render_kw={"class": "btn btn-success"})
    self_lists = SubmitField(self_icon, render_kw={"class": "btn btn-success"})
    available_lists = SubmitField(available_icon, render_kw={"class": "btn btn-success"})
    save = SubmitField('Сохранить', render_kw={"class": "btn btn-success"})


class ChangePassword(FlaskForm):
    id = StringField('list_id', render_kw={"class": "form-control", "hidden": ""})
    password = PasswordField('Пароль', validators=[DataRequired()], render_kw={"class": "form-control"})
    password2 = PasswordField('Повторите пароль', validators=[
                              DataRequired(), EqualTo('password')], render_kw={"class": "form-control"})
    submit = SubmitField('Отправить!', render_kw={"class": "btn btn-lg btn-primary btn-block"})
